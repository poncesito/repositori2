package pizzaria.chila;

import java.util.Date;

public class ventas {
	private int folio;
	private Date fecha;
	private double monto;
	
	public int getfolio()
	{
		return folio;
	}
	public void setFolio(int _folio)
	{
	folio=_folio;	
	}
	public Date getFecha() {
		return fecha;
	}
	public void setFecha(Date _fecha)
	{
	fecha = _fecha;	
	}
	public double getMoto()
	{
		return monto;
	}
	public void setMonto(double _monto) {
		monto=_monto;
		
	}
	
	
	

}
